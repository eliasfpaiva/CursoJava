package br.com.cursojava.variaveis;

public class VariaveisPrimitivasApp {

	public static void main(String[] args) {
		byte age = 10;
		short height = 30000;
		int distanceBetweenTwoSatellites = 1000000000;
		long distanceBetweenTwoGalaxy = 10000000000L;
		float rateOfInterest = 6/5f;
		double rateOfHik = 456/123d;
		char capitalA = 'A';
		boolean isPossible = true;

		System.out.println(age);
		System.out.println(height);
		System.out.println(distanceBetweenTwoSatellites);
		System.out.println(distanceBetweenTwoGalaxy);
		System.out.println(rateOfInterest);
		System.out.println(rateOfHik);
		System.out.println(capitalA);
		System.out.println(isPossible);
	}

}
